const fs = require("node:fs");
const path = require("node:path");

const projectRoot = path.resolve(__dirname, "..");
const sources = [
  "src/core/ui/bootstrap.js",
  "src/config/client.js",
  "src/crawl/client.js",
  "src/results/client.js",
  "src/core/ui/carousel.js",
  "src/core/ui/helpers.js",
  "src/core/ui/tags.js",
  "src/search/client.js",
  "src/core/ui/init.js",
  "src/downloaded/client.js",
  "src/actresses/client.js",
];

const banner = "// Generated by scripts/build-renderer.js. Edit the feature client files under src/.\n\n";
const output = banner + sources
  .map((source) => {
    const absolutePath = path.join(projectRoot, source);
    return `// Source: ${source}\n${fs.readFileSync(absolutePath, "utf-8").trim()}\n`;
  })
  .join("\n");

fs.writeFileSync(path.join(projectRoot, "public/app.js"), output, "utf-8");
console.log(`Built public/app.js from ${sources.length} feature modules`);
